package employee.tracker.model;

/**
 * ShiftType Enum - Defines the type of work shift
 */
public enum ShiftType {
    
    /**
     * MORNING - Early shift (typically 6am - 2pm or 9am - 5pm)
     */
    MORNING,

    /**
     * EVENING - Late afternoon/evening shift (typically 2pm - 10pm)
     */
    EVENING,

    /**
     * NIGHT - Overnight shift (typically 10pm - 6am)
     */
    NIGHT;
}
